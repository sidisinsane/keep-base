"""Tests for `keep.cli`."""

from __future__ import annotations

import sys
import json
import subprocess

from pathlib import Path


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _run_keep(*args: str, cwd: Path) -> subprocess.CompletedProcess[str]:
    """Run the `keep` CLI as a subprocess.

    Args:
        *args: Subcommand and arguments to pass to `keep`.
        cwd: Working directory to run from (should be a workspace root).

    Returns:
        Completed process result.
    """
    return subprocess.run(  # noqa: S603
        [sys.executable, "-m", "keep.cli", *args],
        cwd=cwd,
        capture_output=True,
        text=True,
    )


# ---------------------------------------------------------------------------
# Workspace resolution
# ---------------------------------------------------------------------------


class TestWorkspaceResolution:
    """CLI behaviour when keep.yml is present or absent."""

    def test_exits_with_error_when_no_config(self, tmp_path: Path) -> None:
        result = _run_keep("graph", cwd=tmp_path)

        assert result.returncode != 0
        assert "keep.yml" in result.stderr

    def test_error_message_suggests_workspace_root(
        self, tmp_path: Path
    ) -> None:
        result = _run_keep("graph", cwd=tmp_path)

        assert "workspace root" in result.stderr


# ---------------------------------------------------------------------------
# keep graph
# ---------------------------------------------------------------------------


class TestCmdGraph:
    """CLI `keep graph` subcommand."""

    def test_graph_exits_zero(self, workspace: Path) -> None:
        result = _run_keep("graph", cwd=workspace)

        assert result.returncode == 0

    def test_graph_creates_graph_json(self, workspace: Path) -> None:
        _run_keep("graph", cwd=workspace)

        assert (workspace / ".keep" / "graph.json").exists()

    def test_graph_output_reports_node_count(self, workspace: Path) -> None:
        result = _run_keep("graph", cwd=workspace)

        assert "node(s)" in result.stdout

    def test_graph_output_reports_edge_count(self, workspace: Path) -> None:
        result = _run_keep("graph", cwd=workspace)

        assert "edge(s)" in result.stdout

    def test_graph_json_is_valid(self, workspace: Path) -> None:
        _run_keep("graph", cwd=workspace)
        content = (workspace / ".keep" / "graph.json").read_text(
            encoding="utf-8"
        )
        parsed = json.loads(content)

        assert "nodes" in parsed
        assert "edges" in parsed


