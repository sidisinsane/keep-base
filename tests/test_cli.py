# ---------------------------------------------------------------------------
# keep lint
# ---------------------------------------------------------------------------


class TestCmdLint:
    """CLI `keep lint` subcommand."""

    def test_lint_exits_zero_on_clean_workspace(self, workspace: Path) -> None:
        result = _run_keep("lint", cwd=workspace)

        assert result.returncode == 0

    def test_lint_creates_lint_json(self, workspace: Path) -> None:
        _run_keep("lint", cwd=workspace)

        assert (workspace / ".keep" / "lint.json").exists()

    def test_lint_output_reports_summary(self, workspace: Path) -> None:
        result = _run_keep("lint", cwd=workspace)

        assert "document(s)" in result.stdout

    def test_lint_exits_nonzero_on_violations(self, workspace: Path) -> None:
        # Introduce a dangling slug in a published document.
        ragu_path = workspace / "recipes" / "ragu.md"
        content = ragu_path.read_text(encoding="utf-8")
        ragu_path.write_text(
            content.replace("target: soffritto", "target: ghost-doc"),
            encoding="utf-8",
        )

        result = _run_keep("lint", cwd=workspace)

        assert result.returncode == 1

    def test_lint_output_shows_violation_details(self, workspace: Path) -> None:
        ragu_path = workspace / "recipes" / "ragu.md"
        content = ragu_path.read_text(encoding="utf-8")
        ragu_path.write_text(
            content.replace("target: soffritto", "target: ghost-doc"),
            encoding="utf-8",
        )

        result = _run_keep("lint", cwd=workspace)

        assert "dangling_slug" in result.stdout

    def test_lint_report_written_to_keep_dir(self, workspace: Path) -> None:
        _run_keep("lint", cwd=workspace)
        content = (workspace / ".keep" / "lint.json").read_text(
            encoding="utf-8"
        )
        parsed = json.loads(content)

        assert "summary" in parsed
        assert "documents" in parsed


