class TestGlobalFlags:
    """Global CLI flags."""

    def test_help_exits_zero(self, workspace: Path) -> None:
        result = _run_keep("--help", cwd=workspace)

        assert result.returncode == 0

    def test_help_lists_subcommands(self, workspace: Path) -> None:
        result = _run_keep("--help", cwd=workspace)

        assert "graph" in result.stdout
        assert "index" in result.stdout
        assert "lint" in result.stdout
        assert "state" in result.stdout

    def test_verbose_flag_accepted(self, workspace: Path) -> None:
        result = _run_keep("--verbose", "graph", cwd=workspace)

        assert result.returncode == 0

    def test_no_subcommand_exits_nonzero(self, workspace: Path) -> None:
        result = _run_keep(cwd=workspace)

        assert result.returncode != 0
